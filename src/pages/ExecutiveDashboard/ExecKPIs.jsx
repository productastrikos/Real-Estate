export default function ExecKPIs({ kpis }) {
  const [drilldown, setDrilldown] = useState(null);

  return (
    <>
      <div className="kpi-grid-5">
        {kpis.map((k) => {
          const color = ICON_COLORS[k.icon] || "#5b8de0";
          const ragColor = k.rag === "success" ? "#16a34a" : k.rag === "warning" ? "#d97706" : k.rag === "danger" ? "#dc2626" : color;
          return (
            <div
              key={k.id}
              className="stat-mini-card"
              style={{ borderTop: `2px solid ${ragColor}`, cursor: "pointer" }}
              onClick={() => setDrilldown(k)}
              tabIndex={0}
              onKeyDown={(e) => e.key === "Enter" && setDrilldown(k)}
              role="button"
              aria-label={`${k.label}: ${k.value}. Click for details.`}
            >
              <div style={{ display: "flex", alignItems: "center", gap: 6, marginBottom: 6 }}>
                <div
                  style={{
                    width: 26,
                    height: 26,
                    borderRadius: 8,
                    background: ragColor,
                    display: "flex",
                    alignItems: "center",
                    justifyContent: "center",
                    color: "#fff",
                  }}
                >
                  <Ico name={k.icon} size={13} />
                </div>
                <span className="stat-mini-card__label" style={{ fontSize: 9 }}>
                  {k.label}
                </span>
              </div>
              <span className="stat-mini-card__value" style={{ color: ragColor, fontSize: 19 }}>
                {k.value}
              </span>
              <span className={`stat-mini-card__trend ${k.trendPos ? "pos" : "neg"}`}>{k.trend}</span>
              <span className="stat-mini-card__sub">{k.caption}</span>
            </div>
          );
        })}
      </div>
      {drilldown && (
        <DrilldownModal
          kpi={{
            icon: drilldown.icon,
            status: drilldown.rag?.toUpperCase(),
            ragStatus: drilldown.rag,
            value: drilldown.value,
            trendPct: drilldown.trend,
            trendPos: drilldown.trendPos,
            trendCaption: drilldown.caption,
            sparkData: (["score", "capex", "land", "permitting", "construction"].includes(drilldown.id)
              ? buildExecDrillContent(drilldown)
              : buildDrillContent(drilldown, null)
            ).sparkData,
          }}
          directContent={
            ["score", "capex", "land", "permitting", "construction"].includes(drilldown.id)
              ? buildExecDrillContent(drilldown)
              : buildDrillContent(drilldown, null)
          }
          onClose={() => setDrilldown(null)}
        />
      )}
    </>
  );
}
