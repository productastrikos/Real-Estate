/* Parse numeric from strings like "16.2%", "$163M", "91/100", "Ready" */
function parseKpiNum(val) {
  if (typeof val === "number") return val;
  const s = String(val);
  if (s === "Ready") return 90;
  if (s === "Conditional") return 55;
  if (s === "Not Ready") return 20;
  const m = s.match(/[\d.]+/);
  return m ? parseFloat(m[0]) : 0;
}

